<p>The relationship between the two is rarely well understood and therefore the source of poor management decisions. Management controls only two variables – Price and Quantity. For example, it decides what quantity of labor to use (people hired) and what price to pay (wages or salaries). Conversely, it decides what products or services it offers to the market at what price and at in what quantities. The strategic configuration of these four variables determine sustainable corporate profitability. In traditional accounting, value is the product of quantity x price. Profitability is determined by maximizing the difference between the value received from the market and the value surrendered to the providers of input (materials, labor, etc.).</p>
</div>

