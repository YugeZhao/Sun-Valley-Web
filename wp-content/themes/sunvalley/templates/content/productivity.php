<div class="row type-1">
	<p>The relationship of the three ratios explain bottom-line profit in terms of managerial effectiveness rather than accounting outcomes. Profits can be enhanced by increasing sales prices while decreasing the cost of input resources. Profits can also be enhanced by increasing the quantity of output while decreasing the quantity of input. The former option is unsustainable as it will attract competition very quickly or price your products out of the market.</p>
	
	<p>The latter option is the only sustainable long-term strategy as gains in productivity enables reduction in price recovery. This increases market penetration and sustains competitive pricing. Every management team worthy of the title should be executing a productivity improvement strategy.</p> 
