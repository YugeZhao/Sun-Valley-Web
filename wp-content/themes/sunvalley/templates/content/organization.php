</div>
<div class="row type-3">
	<div class="box content">
		<p>Eliminating cost vs. not spending money</p>
	</div>
</div>
<div class="row type-1">
	<p>The conventional budgeting process which favors budgets by function rather than system, inevitably results in give-and-take negotiation between management and functional heads. To preserve headcount or continue to support projects, budgets tend to be overstated and resources deployed to deliver projects that don’t truly deliver business results. The result is money spent on resources and projects that do not have “line-of-sight” contribution to profitability. Many projects are justified on “soft cost” savings, which never seem to materialize.</p>
	<p>Conversely, eliminating cost incurred in the process of creating the service or product should be relentlessly analyzed and removed.</p>
	<p>To best manage both cost drivers, the fundamental cost driver and cost allocation model should be changed. In the new reality of fast changing demands and expectations make traditional cost allocation models and decisions irrelevant. We have developed business system-based cost allocation and cost management models that support effective cost management.</p>
</div>
<div class="row type-3">
	<div class="box content">
		<p>Organization design by system vs. by function</p>
	</div>
</div>
<div class="row type-1">
	<p>The second managerial failure mode is the constant re-organization of roles, responsibilities and reporting lines. This again the result of fast-changing demands and expectations. To insure costs are minimized and matched to activities, our approach to business as a set of systems also enables the organization to structure work and responsibility by business system and not by function or process.</p>
