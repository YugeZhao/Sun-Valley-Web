<p>While manufacturing processes strived to produce at the highest rates of capacity utilization and process.  efficiency, the output frequently was desynchronized from the variations in demand. With the advent of Lean, inventory became a liability rather than an asset. However, low inventory requires one of two conditions to exist: Predictable demand without variation or extremely responsive and adaptable supply chains. The constant conflict between low cost through efficiency gains and high responsiveness through zero stock-out availability is distilled in the current assets of the balance sheet.</p>

<p>The level of current assets is the level of compromise between the need for efficiency and the need for effectiveness.</p>
