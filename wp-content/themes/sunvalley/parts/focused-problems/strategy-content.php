<p>Cost reduction is not one of them. Constant re-organization is not that either. Setting stretch goals is not.</p>

<p>To determine which constraint your company should own, the following steps apply:</p>

<ul>
	<li>
		<p>Identify, understand the large business systems in your competitive space.</p>
	</li>
	<li>
		<p>Determine which system can or will provide the greatest value to your customer base.</p>
	</li>
	<li>
		<p>Develop a strategy for owning the constraining system or capability in the long term.</p>
	</li>
	<li>
		<p>Execute and constantly realign priorities, projects and resources to reinforce the company’s control of the asset.</p>
	</li>
</ul>

