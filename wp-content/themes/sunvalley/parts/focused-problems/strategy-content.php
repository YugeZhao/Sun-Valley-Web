<p>With the advent of massive amounts of information through the internet, asset or competency-based strategies are frequently known and copied or negated before they even exist long enough to succeed.</p>

<p>The new requirement is to determine what constraint the company must own and control. Instead of thinking about competitive advantage, the focus on competitor disadvantage. The rise of Amazon is proof of this concept. Jeff Bezos became the world’s wealthiest individual by focusing on only one core capability that no competitor can match. Extraordinary customer experience. Your company should be asking itself:</p>

